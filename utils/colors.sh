#!/usr/bin/env bash

ESCAPE=$(printf "\e")

# Reset
CLEAR="${ESCAPE}[0m"

# Regular Colors
BLACK="${ESCAPE}[0;30m"
RED="${ESCAPE}[0;31m"
GREEN="${ESCAPE}[0;32m"
YELLOW="${ESCAPE}[0;33m"
BLUE="${ESCAPE}[0;34m"
PURPLE="${ESCAPE}[0;35m"
CYAN="${ESCAPE}[0;36m"
WHITE="${ESCAPE}[0;37m"
GRAY="${ESCAPE}[1;30m"
